CREATE TABLE TC_NHOM (
    ID NUMBER(22,0) GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    NAM_HOC NUMBER(5,0) NOT NULL,
    HOC_KY NUMBER(2,0),
    TEN NVARCHAR2(128) NOT NULL,
    NHOM_CHA NUMBER(22,0),
    HE_SO NUMBER(5,0),
    CONSTRAINT PK_TC_NHOM PRIMARY KEY (ID)
);
/

CREATE TABLE TC_NHOM_NGANH (
    ID NUMBER(22,0) GENERATED ALWAYS AS IDENTITY (START WITH 1 INCREMENT BY 1) NOT NULL,
    NGANH NVARCHAR2(20) NOT NULL,
    NHOM NUMBER(22,0),
    CONSTRAINT PK_TC_NHOM_NGANH PRIMARY KEY (ID)
);
/